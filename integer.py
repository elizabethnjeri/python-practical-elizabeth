# integer.py
# Demonstrates the integer data type and arithmetic operations in Python

# Creating integer variables
a = 20
b = 6

# Displaying the variables and their type
print("a =", a)
print("b =", b)
print("Type of a:", type(a))

# Addition
addition = a + b
print("Addition (a + b):", addition)

# Subtraction
subtraction = a - b
print("Subtraction (a - b):", subtraction)

# Multiplication
multiplication = a * b
print("Multiplication (a * b):", multiplication)

# Division (true division - always returns a float)
division = a / b
print("Division (a / b):", division)

# Modulus (remainder after division)
modulus = a % b
print("Modulus (a % b):", modulus)

# Floor division (division that rounds down to the nearest whole number)
floor_division = a // b
print("Floor Division (a // b):", floor_division)
